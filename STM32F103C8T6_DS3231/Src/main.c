/** Hàm chuyển đổi giá trị BCD sang số thập phân (DEC)
*/
uint8_t BCD2DEC(uint8_t BCD_NUMBER)
{
	return (BCD_NUMBER>>4)*10+(BCD_NUMBER&0xf);
}
/** Hàm chuyển đổi giá trị thập phân sang số BCD 
***DEC_NUMBER số <100
*/

uint8_t DEC2BCD(uint8_t DEC_NUMBER)
{
	return (DEC_NUMBER/10)<<4|(DEC_NUMBER%10);
}
